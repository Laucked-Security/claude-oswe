// Dev-only. Regenerates ./validators.mjs as a SELF-CONTAINED, zero-runtime-dependency ESM file.
// Strategy: AJV 2020 standalone code generation, then hand-inline the single runtime helper AJV
// references (`ajv/dist/runtime/ucs2length`). No esbuild, no bundler — the standalone output for
// these schemas pulls in exactly one tiny pure function, so inlining it yields a file with zero
// `import`/`require` and no node_modules dependency at runtime. AJV is needed only to GENERATE.
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const Ajv = require("ajv/dist/2020");
const standaloneCode = require("ajv/dist/standalone").default;

const here = dirname(fileURLToPath(import.meta.url));
const schemasDir = join(here, "..", "schemas");

// Map schema $id -> exported JS identifier in validators.mjs
const EXPORT_NAME = {
  "finding.schema.json": "finding",
  "final-finding.schema.json": "finalFinding",
  "analyzer-response.schema.json": "analyzerResponse",
  "chain.schema.json": "chain",
  "verdict.schema.json": "verdict",
  "verifier-response.schema.json": "verifierResponse",
  "report-summary.schema.json": "reportSummary",
  "report.schema.json": "report",
  "sarif-lead.schema.json": "sarifLead",
  "checkpoint-manifest.schema.json": "checkpointManifest"
};

const ajv = new Ajv({ code: { source: true, esm: true }, allErrors: true, strict: false });
for (const file of readdirSync(schemasDir).filter((f) => f.endsWith(".schema.json"))) {
  const schema = JSON.parse(readFileSync(join(schemasDir, file), "utf8"));
  ajv.addSchema(schema, schema.$id);
}
const exportMap = Object.fromEntries(Object.entries(EXPORT_NAME).map(([id, name]) => [name, id]));
let code = standaloneCode(ajv, exportMap);

// The standalone output references exactly one runtime helper via require(). Inline it and assert
// nothing else slipped in, so the result is genuinely dependency-free.
const RUNTIME_REQUIRE = 'require("ajv/dist/runtime/ucs2length").default';
if (!code.includes(RUNTIME_REQUIRE)) {
  throw new Error("expected ucs2length require not found — AJV output shape changed; re-check inlining");
}
code = code.split(RUNTIME_REQUIRE).join("_ucs2length");
code = code.replace(/^"use strict";/, ""); // ESM is always strict; drop the redundant leading directive
for (const m of code.matchAll(/(?:^|[^.\w])(?:require|import)\s*[(\s]/g)) {
  throw new Error("residual require/import in generated code — not self-contained: " + m[0]);
}

// https://github.com/ajv-validator/ajv  (MIT License) — ucs2length runtime helper, inlined verbatim.
const ucs2length = `function _ucs2length(str) {
  const len = str.length; let length = 0; let pos = 0; let value;
  while (pos < len) {
    length++; value = str.charCodeAt(pos++);
    if (value >= 0xd800 && value <= 0xdbff && pos < len) {
      value = str.charCodeAt(pos);
      if ((value & 0xfc00) === 0xdc00) pos++;
    }
  }
  return length;
}`;

